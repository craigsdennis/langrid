package jp.go.nict.langrid.dao.jsonic.ws;

import java.io.File;
import java.io.IOException;

import javax.servlet.Filter;
import javax.servlet.FilterChain;
import javax.servlet.FilterConfig;
import javax.servlet.ServletException;
import javax.servlet.ServletRequest;
import javax.servlet.ServletResponse;

import jp.go.nict.langrid.dao.jsonic.JsonicDaoContext;

public class Configurator implements Filter{
	@Override
	public void destroy() {
	}

	@Override
	public void doFilter(ServletRequest arg0, ServletResponse arg1,
			FilterChain arg2) throws IOException, ServletException {
		arg2.doFilter(arg0, arg1);
	}

	@Override
	public void init(FilterConfig arg0) throws ServletException {
		JsonicDaoContext.setContextDir(new File(
				arg0.getServletContext().getRealPath("WEB-INF")
				));
	}
}
